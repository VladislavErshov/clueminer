    @Override
    public void resultChanged(LookupEvent ev) {

        if (result != null) {
            final Collection<? extends Clustering> res = result.allInstances();
            if (res != null) {
                SwingUtilities.invokeLater(new Runnable() {
                    @Override
                    public void run() {
                        panel.setReferenceClustering(res);
                    }
                });
            }

        }
    }
